#!/usr/bin/env python3
"""
2-floor.py

This module provides a function to compute the floor of a float.
"""

import math


def floor(n: float) -> int:
    """
    Returns the floor of a float.

    Parameters:
    n (float): The float number to floor.

    Returns:
    int: The floor of the float number.
    """
    return math.floor(n)
