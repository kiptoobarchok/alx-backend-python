#!/usr/bin/env python3
"""
3-to_str.py

This module provides a function to convert a float to its string rep.
"""


def to_str(n: float) -> str:
    """
    Converts a float to its string representation.

    Parameters:
    n (float): The float number to convert.

    Returns:
    str: The string representation of the float number.
    """
    return str(n)
